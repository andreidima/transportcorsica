Sunt acceptate caracterele definite in codarea standard GSM si anume GSM 7-bit encoding (IA5) (data coding 0), fiind tabela standard de caractere pe care o accepta si operatorii de telefonie mobila si fiind suportata de majoritatea telefoanelor mobile.
<br><br>
De asemenea standardul GSM 7-bit encoding (IA5) defineste urmatoarele caracteristici pentru textul SMS-ului:
<br>
(1) <b>Lungimea maxima a unui SMS este de 160 de caractere.</b>
<br>
(2) SMS-urile concatenate mai lungi de 160 de caractere sunt compuse din segmente (parti) a caror lungime maxima este de 152 de caractere. <b>Lungimea maxima a unui SMS concatenat suportata de toti operatorii este de 608 caractere (4 SMS)</b>.
<br><br>
Caracterele <b>[ ] { } | ~ ^ " ` CR (Carriage Return) si LF (Line Feed) vor ocupa spatiul a doua caractere in textul SMS-ului</b>.
